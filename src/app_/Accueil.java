    private void kButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_kButton3ActionPerformed
        String id = jTextField1.getText();
        String nom = jTextField6.getText();
        String prenom = jTextField3.getText();
        String age = jTextField3.getText();
        String tel = jTextField4.getText();
        try {
            if (id.equals("") || nom.equals(null) || prenom.equals("") || age.equals("") || tel.equals("")) {
                JOptionPane.showMessageDialog(null, "Veuillez renseigner tout les champs\n\nN.B: Si un champ est nulle veuillez taper 0");
            } else {
                if (age.matches("[0-9]{2}")) {
                    Connection c = cnx.connect();
                    Statement st = c.createStatement();
                    ResultSet rs = st.executeQuery("select * from gardiens where cin='" + jTextField1.getText() + "'");
                    if (rs.next()) {
                        JOptionPane.showMessageDialog(null, "Gardien " + jTextField1.getText() + " existe déjà!");
                        jTextField1.setText("");
                        jTextField2.setText("");
                        jTextField3.setText("");
                        jTextField4.setText("");
                        jTextField6.setText("");
                        jComboBox1.setSelectedIndex(0);
                    } else {
                        int nb = st.executeUpdate("INSERT INTO gardiens values('" + jTextField1.getText() + "','" + jTextField6.getText() + "','" + jTextField2.getText() + "'," + jTextField3.getText() + ",'" + jTextField4.getText() + "','" + jComboBox1.getSelectedItem() + "')");
                        if (nb != 0) {
                            JOptionPane.showMessageDialog(null, "Gardien " + jTextField1.getText() + " a été ajouté avec succés!");
                            kButton6ActionPerformed(evt);
                            jTextField1.setText("");
                            jTextField2.setText("");
                            jTextField3.setText("");
                            jTextField4.setText("");
                            jTextField6.setText("");
                            jComboBox1.setSelectedIndex(0);
                        } else {
                            JOptionPane.showMessageDialog(null, "Gardien non ajouté!");
                        }
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "L'âge " + age + " est inacceptable!");
                    jTextField3.setText("");
                }
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_kButton3ActionPerformed
