    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        try {
            Connection c = cnx.connect();
            Statement st = c.createStatement();
            ResultSet rs = st.executeQuery("select * from gardiens");
            DefaultTableModel dtm = new DefaultTableModel();
            ResultSetMetaData rsmd = rs.getMetaData();
            int nc = rsmd.getColumnCount();
            String[] entete = new String[nc];
            for (int i = 0; i < nc; i++) {
                entete[i] = rsmd.getColumnName(i + 1);
            }
            dtm.setColumnIdentifiers(entete);
            while (rs.next()) {
                String[] ligne = new String[nc];
                for (int i = 0; i < nc; i++) {
                    ligne[i] = rs.getString(i + 1);
                }
                dtm.addRow(ligne);
            }
            jTable1.setModel(dtm);
            c.close();
            st.close();
            rs.close();
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_formWindowOpened

