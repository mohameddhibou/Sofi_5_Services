    private void kButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_kButton4ActionPerformed
        try {
            if (count == 1) {
                String CIN = JOptionPane.showInputDialog("Veuillez saisir le CIN du Gardien que vous voulez Mofidier:");
                Connection c = cnx.connect();
                Statement st = c.createStatement();
                ResultSet rs = st.executeQuery("select * from gardiens where cin='" + CIN + "'");
                if (rs.next()) {
                    jTextField1.setText(rs.getString(1));
                    jTextField1.disable();
                    jTextField6.setText(rs.getString(2));
                    jTextField2.setText(rs.getString(3));
                    jTextField3.setText(rs.getString(4));
                    jTextField4.setText(rs.getString(5));
                    jComboBox1.setSelectedItem(rs.getString(6));
                    kButton4.setText("Valider Modif..");
                    count = 0;
                    c.close();
                    st.close();
                    rs.close();
                    return;
                } else {
                    JOptionPane.showMessageDialog(null, "Gardien " + CIN + " Introuvable!");
                }

            } else if (count == 0) {
                /*kButton4.setkBackGroundColor(new Color(192,192,192));
                kButton4.setkStartColor(new Color(192,192,192));
                kButton4.setkEndColor(new Color(192,192,192));*/
                Connection c = cnx.connect();
                Statement st = c.createStatement();
                int nb = st.executeUpdate("update gardiens set nom='" + jTextField6.getText() + "', prenom='" + jTextField2.getText() + "', age=" + jTextField3.getText() + ", tel='" + jTextField4.getText() + "', genre='" + jComboBox1.getSelectedItem() + "' where cin='" + jTextField1.getText() + "'");
                if (nb != 0) {
                    JOptionPane.showMessageDialog(null, "Gardien " + jTextField1.getText() + " a été modifier avec succés!");
                    kButton6ActionPerformed(evt);
                    kButton4.setText("Modifier Gardien");
                    jTextField1.enable();
                    jTextField1.setText("");
                    jTextField2.setText("");
                    jTextField3.setText("");
                    jTextField4.setText("");
                    jTextField6.setText("");
                    jComboBox1.setSelectedIndex(0);
                    count = 1;
                    return;
                }
            }

        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_kButton4ActionPerformed

